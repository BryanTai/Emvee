//A representation of all the symbols an Advancement can have.
//A PlayerCard's Symbol count is a sum of all the Symbols on it's Advancements

function Symbols(){
    this.mana = 0,
    this.guardian = 0,
    //Spoil
    this.decay = 0,
    this.growth = 0,
    //Spirits
    this.green = 0,
    this.yellow = 0,
    this.brown = 0,
    this.wild = 0,
    //Victory Points
    this.blueVP = 0,
    this.grayVP = 0
}